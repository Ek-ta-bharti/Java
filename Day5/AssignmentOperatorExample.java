package Day5;
public class AssignmentOperatorExample {
    public static void main(String[] args) {
        int n1, n2, n3, n4;
        n1 = n2 = n3 = n4 = 10;
        System.out.println(n1 + " " + n2 + " " + n3 + " " + n4);
        int a = 10;
        a += 5;
        System.out.println(a);

        int b = 10;
        b -= 5;
        System.out.println(b);

        int c = 10;
        c /= 5;
        System.out.println(c);
    }

}
